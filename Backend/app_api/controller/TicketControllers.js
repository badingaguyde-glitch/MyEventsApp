// @desc    Check ticket availability for event
// @route   GET /api/tickets/event/:eventId/availability
// @access  Public
const checkAvailability = async (req, res) => {
    try {
        const { eventId } = req.params;

        const event = await Event.findById(eventId);
        if (!event) {
            return res.status(404).json({ message: 'Event not found' });
        }

        const soldTickets = await Ticket.countDocuments({
            event: eventId,
            status: { $ne: 'cancelled' }
        });

        const available = event.capacity - soldTickets;

        res.json({
            eventId: event._id,
            eventTitle: event.title,
            totalCapacity: event.capacity,
            soldTickets,
            availableSpots: available,
            isSoldOut: available <= 0
        });
    } catch (error) {
        console.error('Check availability error:', error);
        res.status(500).json({ 
            message: 'Server error',
            error: error.message 
        });
    }
};

// @desc    Bulk verify tickets (for scanning multiple tickets)
// @route   POST /api/tickets/bulk-verify
// @access  Private
const bulkVerifyTickets = async (req, res) => {
    try {
        const { eventId, ticketCodes } = req.body;

        if (!eventId || !ticketCodes || !Array.isArray(ticketCodes)) {
            return res.status(400).json({ 
                message: 'Event ID and ticket codes array required' 
            });
        }

        // Check if event exists and user is organizer
        const event = await Event.findById(eventId);
        if (!event) {
            return res.status(404).json({ message: 'Event not found' });
        }

        if (event.organizer.toString() !== req.user.id && req.user.role !== 'admin') {
            return res.status(403).json({ 
                message: 'Not authorized to verify tickets for this event' 
            });
        }

        const results = [];
        const now = new Date();

        for (const code of ticketCodes) {
            const ticket = await Ticket.findOne({ 
                ticketCode: code.toUpperCase(),
                event: eventId
            }).populate('user', 'firstName lastName email');

            if (!ticket) {
                results.push({
                    ticketCode: code,
                    valid: false,
                    message: 'Invalid ticket code'
                });
                continue;
            }

            if (ticket.status === 'used') {
                results.push({
                    ticketCode: code,
                    valid: false,
                    message: 'Ticket already used',
                    ticket: {
                        user: ticket.user,
                        checkInTime: ticket.checkInTime
                    }
                });
                continue;
            }

            if (ticket.status === 'cancelled') {
                results.push({
                    ticketCode: code,
                    valid: false,
                    message: 'Ticket cancelled',
                    ticket: { user: ticket.user }
                });
                continue;
            }

            // Mark as used
            ticket.status = 'used';
            ticket.checkInTime = now;
            ticket.checkedInBy = req.user.id;
            await ticket.save();

            results.push({
                ticketCode: code,
                valid: true,
                message: 'Successfully checked in',
                ticket: {
                    user: ticket.user,
                    checkInTime: now
                }
            });
        }

        res.json({
            totalProcessed: results.length,
            successful: results.filter(r => r.valid).length,
            failed: results.filter(r => !r.valid).length,
            results
        });
    } catch (error) {
        console.error('Bulk verify error:', error);
        res.status(500).json({ 
            message: 'Server error during bulk verification',
            error: error.message 
        });
    }
};

